package com.example.campushelper.model;

public class Student {
	private String StuId;   //学生编号
	private String StuName; //学生姓名 
	private String StuPsd;   //学生密码
	private String StuMajor; //学生专业
	private String StuClass; //学生年级
	private String StuGrade; //学生班级
	public String getStuId() {
		return StuId;
	}
	public void setStuId(String stuId) {
		StuId = stuId;
	}
	public String getStuName() {
		return StuName;
	}
	public void setStuName(String stuName) {
		StuName = stuName;
	}
	public String getStuPsd() {
		return StuPsd;
	}
	public void setStuPsd(String stuPsd) {
		StuPsd = stuPsd;
	}
	public String getStuMajor() {
		return StuMajor;
	}
	public void setStuMajor(String stuMajor) {
		StuMajor = stuMajor;
	}
	public String getStuClass() {
		return StuClass;
	}
	public void setStuClass(String stuClass) {
		StuClass = stuClass;
	}
	public String getStuGrade() {
		return StuGrade;
	}
	public void setStuGrade(String stuGrade) {
		StuGrade = stuGrade;
	}
	
}
